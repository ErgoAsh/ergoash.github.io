export class Point {
    public x: number;
    public y: number;

    constructor(x: number, y: number) {
        this.x = x;
        this.y = y;
    }
}

export class PolarPoint {
    public rho: number;
    public theta: number;

    constructor(rho: number, theta: number) {
        this.rho = rho;
        this.theta = theta;
    }
}
